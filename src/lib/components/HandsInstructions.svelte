  <h1>Hands Instructions</h1>
  <p>Use your hands to control the broomstick!</p>
</div>

<div>
  <h2>Magical Gestures</h2>
  <ul>
    <li><strong>Point with Index Finger</strong> turns the broomstick relative to the angle of your finger.</li>
    <li><strong>Point with Index and Middle Fingers</strong> to ascend on the broomstick.</li>
    <li><strong>Point with Index, Middle and Ring fingers</strong> to descend on the broomstick.</li>
    <li><strong>Open Palm</strong> to pause / resume the game.</li>
    <li><strong>Thumbs Up</strong> to play again after winning.</li>
  </ul>
</div>